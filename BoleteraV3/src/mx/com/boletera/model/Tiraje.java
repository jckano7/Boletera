package mx.com.boletera.model;

/**
 *
 * @author jkano
 */
public class Tiraje {
    private Integer idTiraje;
    private Integer folioInicial;
    private Integer folioFinal;
    private Integer numDigitos;
    private Integer numFolios;
    /**
     * @return the idTiraje
     */
    public Integer getIdTiraje() {
        return idTiraje;
    }

    /**
     * @param idTiraje the idTiraje to set
     */
    public void setIdTiraje(Integer idTiraje) {
        this.idTiraje = idTiraje;
    }

    /**
     * @return the folioInicial
     */
    public Integer getFolioInicial() {
        return folioInicial;
    }

    /**
     * @param folioInicial the folioInicial to set
     */
    public void setFolioInicial(Integer folioInicial) {
        this.folioInicial = folioInicial;
    }

    /**
     * @return the folioFinal
     */
    public Integer getFolioFinal() {
        return folioFinal;
    }

    /**
     * @param folioFinal the folioFinal to set
     */
    public void setFolioFinal(Integer folioFinal) {
        this.folioFinal = folioFinal;
    }

    /**
     * @return the numDigitos
     */
    public Integer getNumDigitos() {
        return numDigitos;
    }

    /**
     * @param numDigitos the numDigitos to set
     */
    public void setNumDigitos(Integer numDigitos) {
        this.numDigitos = numDigitos;
    }

    /**
     * @return the numFolios
     */
    public Integer getNumFolios() {
        return numFolios;
    }

    /**
     * @param numFolios the numFolios to set
     */
    public void setNumFolios(Integer numFolios) {
        this.numFolios = numFolios;
    }
    
}
